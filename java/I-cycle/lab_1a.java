                          /* TO PRINT THE LEFT SIDED PYRAMID */

import java.io.*;
class lab_1a
{
   public static void main(String args[])
   {
       for(int i=0;i<5;i++)          /* FOR LOOP */
       {
           for(int j=0;j<=i;j++)
           {
               System.out.print("* ");
           }
           System.out.println();
       }
   }
}               
